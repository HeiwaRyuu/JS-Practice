// Declaring Variables and they're assignments

var a = 1;
var b = "Vitor";
var c = true;

// As you can see, JavaScript does not require you to declare the type of data the variable will store as you need to do in C or Java

// You can also declare many variables in a single line separated by commas
 
var a = 1, b = "Vitor", c = true;

// JavaScript is also a case sensitive program, meaning that a varible "a" is not equal to a variable "A"

var a = 1;
var A = 4;

// Both variables above are read and interpreted as different variables by JavaScript
